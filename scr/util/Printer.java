package util;

/**
 * The Printer class provides utility methods for printing messages to the
 * console.
 * 
 * This class contains static methods to print different messages to the
 * console.
 * 
 * @author Jan Wieland
 * @version 1.0
 */
public class Printer {

    /**
     * Prints "Hello World!" to the console.
     */
    public static void printHelloWorld() {
        System.out.println("Start Calculator");
    }

    /**
     * Prints the specified text to the console.
     *
     * @param text The text to be printed to the console.
     */
    public static void printText(String text) {
        System.out.println(text);
    }
}
