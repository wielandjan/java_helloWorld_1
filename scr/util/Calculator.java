package util;

/**
 * Utility class for basic arithmetic operations
 * 
 * @author Jan Wieland
 * @version 1.0
 */
public class Calculator {

    /**
     * Adds two integers
     *
     * @param a the first integer to add
     * @param b the second integer to add
     * @return the sum of a and b
     */
    public static int add(int a, int b) {
        return a + b;
    }
}
